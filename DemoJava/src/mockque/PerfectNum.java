package mockque;

public class PerfectNum {
	
	public static void main(String[]args) {
		int z = 5;
	    int sum= 0;
	    
		for(int i =1;i<z;i++) {
			if(z%i==0) {
				sum=sum+i;
			}	
		}
			if(z==sum) 
				System.out.println("perfect");
				else
					System.out.println("not perfect");
			}
		}
		
		
	


